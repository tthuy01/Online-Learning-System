package model;

/**
 *
 * @author Admin
 */
public class Slider {

    private int id;
    private String title;
    private String image;
    private String link;
    private boolean status;
    private String note;

    public Slider() {
    }

    public Slider(int id, String title, String image, String link, boolean status, String note) {
        this.id = id;
        this.title = title;
        this.image = image;
        this.link = link;
        this.status = status;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

}
